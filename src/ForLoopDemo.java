
public class ForLoopDemo {

	public static void main(String[] args) {
		
		//FOR Example
		for (int i=5; i<=10; i++) {
			
			System.out.println("Good morning");
		}
		
		//Print number 1 to 100 (+)
		
		for(int i=1; i<=100; i++) {
		System.out.println(i);
		
	}
		// Print numbers 10 to 0 (-)
		
		for (int i=10; i>10; i--) {
			System.out.println(i+" ");
			// you infinite print out
			
		}
		
		////////////////////////////////////
		
		
		for (int i=10; i<=30; i+=3) {
			System.out.println(i+" ");
		}
		
		
		
		
			
			
				
			}
		
		
		
	
	}


