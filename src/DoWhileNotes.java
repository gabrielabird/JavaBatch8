import java.util.Scanner;

public class DoWhileNotes {

	public static void main(String[] args) {
		// DO WHILE LOOP
		
		int b=20;
		do {
			System.out.println("Hi");
			b++;
		}while(b<15);
		
		//WHILE LOOP
		
		int a=20;
		while(a<15) {
			System.out.println("Hello");
			a++;
		
			// Practice
			
		
		}
		
		}

	}


